// Arithmetic Operators
//  +,-,*,/,**,%
let FirstNumber = 42;
let SecondNumber = 18;
console.log("This is addition : ");
let add = FirstNumber + SecondNumber;
console.log(add);

console.log("This is subs : ");
let sub = FirstNumber - SecondNumber;
console.log(sub);

console.log("This is multiply : ")
let mul = FirstNumber * SecondNumber;
console.log(mul);

console.log("This is division : ");
let div = FirstNumber / SecondNumber;
console.log(div);

console.log("This is modulo : ");
let mod = FirstNumber % SecondNumber;
console.log(mod);

console.log("This is power : ");
let pow = FirstNumber ** SecondNumber;
console.log(pow);

// Unary Operators
//  ++,--
console.log("This is Unary Operators is given below : ")
let a =10;
console.log("Initial value is : " + a);
a++;
console.log("After Increment the value is : " + a);

